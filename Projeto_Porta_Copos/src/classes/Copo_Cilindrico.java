public class Copo_Cilindrico {
    
     private double raio_do_copo_cilindrico;

    public Copo_Cilindrico() {}

    public Copo_Cilindrico(double raio_do_copo_cilindrico) {
        this.raio_do_copo_cilindrico = raio_do_copo_cilindrico;
    }

    // metodo de outro programador
    public double getRaioQ() {
        return raio_do_copo_cilindrico;
    }
    
    // metodo de outro programador
    public double getRaioH() {
        return raio_do_copo_cilindrico;
    }
    
}
